# Example 1: Loop through a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example 2: Loop through a range
for i in range(5):
    print(i)

# Example 3: Loop through a string
for letter in "hello":
    print(letter)

# Example 4: Sum numbers
total = 0
for i in range(1, 6):
    total += i
print("Sum:", total)

# Example 5: Loop through dictionary keys
d = {"a": 1, "b": 2}
for key in d:
    print(key, d[key])
